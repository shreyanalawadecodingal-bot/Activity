# Integer
age = 20
print(age, "is of type", type(age))

# Float
height = 5.7
print(height, "is of type", type(height))

# String
name = "Alice"
print(name, "is of type", type(name))

# Boolean
is_student = True
print(is_student, "is of type", type(is_student))

# List
numbers = [1, 2, 3, 4]
print(numbers, "is of type", type(numbers))

# Tuple
coords = (10, 20)
print(coords, "is of type", type(coords))

# Set
fruits = {"apple", "banana", "cherry"}
print(fruits, "is of type", type(fruits))

# Dictionary
student = {"name": "Alice", "age": 20}
print(student, "is of type", type(student))
