# Integer to float
x = 10
y = float(x)
print(y, type(y))   # Output: 10.0 <class 'float'>

# Float to integer
a = 5.9
b = int(a)
print(b, type(b))   # Output: 5 <class 'int'>

# String to integer
s = "25"
n = int(s)
print(n, type(n))   # Output: 25 <class 'int'>

# Integer to string
num = 100
str_num = str(num)
print(str_num, type(str_num)) # Output: '100' <class 'str'>
