# Create an array (list)
arr = [10, 20, 30, 40, 50]

# Access elements
print("First element:", arr[0])
print("Last element:", arr[-1])

# Add elements
arr.append(60)
print("After append:", arr)

# Remove elements
arr.remove(20)
print("After remove:", arr)

# Iterate
print("Array elements:")
for item in arr:
    print(item)
